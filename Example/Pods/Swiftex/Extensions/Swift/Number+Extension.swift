import Foundation

extension Int {
    
    public var toString: String {
        return String(stringInterpolationSegment: self)
    }
    
}

extension Double {

    public var toString: String {
        return String(stringInterpolationSegment: self)
    }
    
}

extension Float {
    
    public var toString: String {
        return String(stringInterpolationSegment: self)
    }
    
}



